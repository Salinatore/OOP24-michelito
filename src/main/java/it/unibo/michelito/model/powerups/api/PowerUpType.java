package it.unibo.michelito.model.powerups.api;

public enum PowerUpType {
    /**
     * A speed increasing powerUp.
     */
    SPEED_POWERUP,

    /**
     * A powerUp that changes the bomb type placed by the player.
     */
    BOMB_TYPE_POWERUP,

    /**
     * A powerUp that increases the amount of Bombs placeable by the player.
     */
    BOMB_LIMIT_POWERUP,
}
