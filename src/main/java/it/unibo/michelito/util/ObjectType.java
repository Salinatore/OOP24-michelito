package it.unibo.michelito.util;

/**
 * Enumeration containing all types of objects present in the Maze.
 * Used for possible view implementation.
 */
public enum ObjectType {
    /**
     * A player.
     */
    PLAYER,

    /**
     * A box.
     */
    BOX,

    /**
     * A wall.
     */
    WALL,

    /**
     * An enemy.
     */
    ENEMY,

    /**
     * A speed speed powerUp.
     */
    SPEED_POWERUP,

    /**
     * A bomb type powerUp.
     */
    BOMB_TYPE_POWERUP,

    /**
     * A bomb limit poweUp.
     */
    BOMB_LIMIT_POWERUP,

    /**
     * A Door.
     */
    DOOR,

    /**
     * A BlankSpace.
     */
    BLANK_SPACE,

    /**
     * A Flame.
     */
    FLAME,

    /**
     * A Bomb.
     */
    BOMB
}
